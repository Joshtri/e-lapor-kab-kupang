// Fungsi utilitas jika ingin ambil list opsi kategori utama
export const getMainCategories = () => {
  return reportCategories.map((c) => ({
    label: c.text,
    value: c.text,
  }));
};

// Fungsi recursive untuk mengambil semua subkategori dari kategori tertentu
// Fungsi untuk ambil subkategori (semua tingkatan inc)
export const getSubcategoriesByText = (categoryText) => {
    const parent = reportCategories.find((cat) => cat.text === categoryText);
    if (!parent || !parent.sub) return [];
  
    return parent.sub.map((sub) => ({
      label: sub.text,
      value: sub.text,
    }));
  };
  